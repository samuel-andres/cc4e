/* Hello World
 *
 * New lines aren't automatically added
 *
 * %d formats a number
 *
 * %s formats a string
 *
 * %xf formats a floating number with x number of decimals
 *
 * printf write output to stdout
 *
 */

#include <stdio.h>

int main() {
  printf("Hello World\n");
  printf("Answer %d\n", 42);
  printf("Name %s\n", "Sarah");
  printf("x %.1f i %d\n", 3.5, 100);
}
